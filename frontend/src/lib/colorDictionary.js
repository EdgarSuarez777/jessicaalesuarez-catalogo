export function parseColorName(colorName) {
  const name = colorName.toLowerCase().trim();
  
  const pastelMatch = name.match(/^pastel\s+(.+)$/);
  if (pastelMatch) {
    const baseColor = pastelMatch[1];
    const baseHex = colorDictionary[baseColor] || colorDictionary[baseColor.replace(/\s+/g, '_')];
    if (baseHex) {
      return lightenColor(baseHex, 35);
    }
  }
  
  const claroMatch = name.match(/^(.+)\s+claro$/);
  if (claroMatch) {
    const baseColor = claroMatch[1];
    const baseHex = colorDictionary[baseColor] || colorDictionary[baseColor.replace(/\s+/g, '_')];
    if (baseHex) {
      return lightenColor(baseHex, 25);
    }
  }
  
  const oscuroMatch = name.match(/^(.+)\s+oscuro$/);
  if (oscuroMatch) {
    const baseColor = oscuroMatch[1];
    const baseHex = colorDictionary[baseColor] || colorDictionary[baseColor.replace(/\s+/g, '_')];
    if (baseHex) {
      return darkenColor(baseHex, 25);
    }
  }
  
  return colorDictionary[name] || colorDictionary[name.replace(/\s+/g, '_')] || null;
}

function lightenColor(hex, percent) {
  const num = parseInt(hex.replace('#', ''), 16);
  const r = (num >> 16) + Math.round((255 - (num >> 16)) * (percent / 100));
  const g = ((num >> 8) & 0x00FF) + Math.round((255 - ((num >> 8) & 0x00FF)) * (percent / 100));
  const b = (num & 0x0000FF) + Math.round((255 - (num & 0x0000FF)) * (percent / 100));
  return '#' + (0x1000000 | (r << 16) | (g << 8) | b).toString(16).slice(1).toUpperCase();
}

function darkenColor(hex, percent) {
  const num = parseInt(hex.replace('#', ''), 16);
  const r = (num >> 16) - Math.round((num >> 16) * (percent / 100));
  const g = ((num >> 8) & 0x00FF) - Math.round(((num >> 8) & 0x00FF) * (percent / 100));
  const b = (num & 0x0000FF) - Math.round((num & 0x0000FF) * (percent / 100));
  return '#' + (0x1000000 | (r << 16) | (g << 8) | b).toString(16).slice(1).toUpperCase();
}
